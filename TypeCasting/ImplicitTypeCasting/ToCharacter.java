public class ToCharacter
{
  public static void main(String[] args)
  {
    //literals to char variable

    //int literal within char range
    char a1 = 97;

    //int literal outside char range
    char a2 = -97;

    //long literal within char range
    char a3 = 100l;

    //long literal outside char range
    char a4 = -97l;

    //float literal within char range
    char a5 = 97f;

    //float literal outside char range
    char a6 = -97f;

    //double literal within char range
    char a7 = 97D;

    //double literal outside char range
    char a8 = -97D;

    //variables to char variable
    byte b1 = 30;
    short s1 = 20000;
    char c1 = 'a';
    int i1 = 23000000;
    long l1 = 2323232323232323l;
    float f1 = 123.45f;
    double d1 = 1234.5678;

    //byte variable to char variable
    char c2 = b1;

    //short variable to char variable
    c2 = s1;

    //int variable to char variable
    c2 = i1;

    //long variable to char variable
    c2 = l1;

    //float variable to char variable
    c2 = f1;

    //double variable to char variable
    c2 = d1;

  }
}
