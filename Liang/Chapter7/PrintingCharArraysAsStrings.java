public class PrintingCharArraysAsStrings {
	public static void main(String[] args) {
		char[] currencyCode = {'I', 'N', 'R'};
		System.out.println(currencyCode);
		// you can print character arrays as if they are Strings. This is similar to how to we used printf in C. In C, character arrays were strings, there was no defined string type.
	}
}