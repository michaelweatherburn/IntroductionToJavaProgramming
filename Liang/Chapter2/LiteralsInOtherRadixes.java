public class LiteralsInOtherRadixes
{
	public static void main(String[] args)
	{
		//to denote a binary number, use the prefix 0b or 0B before a number
		System.out.println(0B1111);

		//to denote an octal number, use the prefix 0
		System.out.println(056);

		//to denote a hexadecimal number, use the prefix 0x or 0X
		System.out.println(0XF);
	}
}
