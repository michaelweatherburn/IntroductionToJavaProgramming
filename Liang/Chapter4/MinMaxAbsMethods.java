public class MinMaxAbsMethods
{
  public static void main(String[] args)
  {
    //Math.abs returns the absolute value of the input. It "positivizes" the input
    System.out.println(Math.abs(4.5) + " " + Math.abs(-4.5));

    //Math.max returns the maximum value among the two values passed to it
    System.out.println(Math.max(4,6.56));

    //Math.min returns the minimum value among the two values passed to it
    System.out.println(Math.min(4.5,6.56));
  }
}
