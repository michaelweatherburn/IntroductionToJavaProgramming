public class RoundingMethodsInAction
{
  public static void main(String[] args)
  {
    System.out.println(Math.ceil(0.43));
    System.out.println(Math.floor(0.65));
    System.out.println(Math.rint(0.45));
    System.out.println(Math.rint(0.66));
    System.out.println(Math.rint(6.5));
    System.out.println(Math.rint(5.5));
    System.out.println(Math.round(0.4f));
    System.out.println(Math.round(0.6f));
    System.out.println(Math.round(0.4));
    System.out.println(Math.round(0.6));

  }
}
